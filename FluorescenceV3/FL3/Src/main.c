extern volatile uint32_t _bootloader_flag;
/**
 * @brief Write dirty bit and go into DFU mode bootloader
 * @param bootloader_status 
 */
void activate_bootloader(uint32_t bootloader_status) {
	if(bootloader_status == 1) {
		// Write DFU message
		uint8_t dfu_msg[6] = {0x00, 0x00, 0x00, 0x7C, 0x8E, 0x7A};
		NVIC_DisableIRQ(TIM1_UP_TIM16_IRQn);
		HAL_SPI_Transmit(&hspi1, dfu_msg, CONFIG_NUM_DIGITS, 40);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);
		NVIC_EnableIRQ(TIM1_UP_TIM16_IRQn);
		// Disable interrupts
		__disable_irq();
		// http://blog.fahhem.com/2017/12/jump-to-emb-bootloader/
		_bootloader_flag = BOOTLOADER_FLAG_VALUE; // Magic number flag set
		// See you latte
		NVIC_SystemReset();

	  while(1);
	}
}


